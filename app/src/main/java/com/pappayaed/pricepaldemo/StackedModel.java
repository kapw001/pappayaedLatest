public class StackedModel {

    private Float val1;
    private Float val2;

    public StackedModel(Float val1, Float val2) {
        this.val1 = val1;
        this.val2 = val2;
    }

    public Float getVal1() {
        return val1;
    }

    public void setVal1(Float val1) {
        this.val1 = val1;
    }

    public Float getVal2() {
        return val2;
    }

    public void setVal2(Float val2) {
        this.val2 = val2;
    }
}
